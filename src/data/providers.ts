function makeReviews(seed: number, count: number, avg: number): Review[] {
  const reviews: Review[] = [];
  for (let i = 0; i < count; i++) {
    const idx = (seed + i * 3) % REVIEW_COMMENTS.length;
    
    const jitter = (i % 3) - 1;
    const rating = Math.max(3, Math.min(5, Math.round(avg) + jitter));
    const daysAgo = (seed + i * 7) % 90;
    const date = new Date(2026, 4, 1 - daysAgo).toISOString();
    reviews.push({
      id: `r-${seed}-${i}`,
      author: REVIEW_AUTHORS[(seed + i) % REVIEW_AUTHORS.length],
      rating,
      comment: REVIEW_COMMENTS[idx],
      date,
    });
  }
  return reviews;
}

function averageFromBreakdown(b: RatingBreakdown): number {
  const total = b.reduce((a, c) => a + c, 0);
  if (total === 0) return 0;
  const weighted = b.reduce((acc, count, i) => acc + count * (i + 1), 0);
  return Math.round((weighted / total) * 10) / 10;
}

